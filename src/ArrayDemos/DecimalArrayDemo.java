package ArrayDemos;

public class DecimalArrayDemo {

    public static void main(String[] args) {

        double[] d = new double[5];

        d[0] = 1.2;
        d[1] = 3.2;
        d[2] = 1.8;
        d[3] = 5.2;
        d[4] = 9.2;


        for(int i=0;i<d.length;i++)
        {
            System.out.println(d[i]);
        }



    }
}
